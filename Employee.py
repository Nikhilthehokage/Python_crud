class Employee:
	def __init__(self,ID,firstName,lastName,age):
		self.__id=ID
		self.__firstName=firstName
		self.__lastName=lastName
		self.__age=age
	def get_id(self):
		return self.__id
	
	
	
	def get_firstName(self):
		return self.__firstName
	
	
	
	def get_lastName(self):
		return self.__lastName
	
	
	
	def get_age(self):
		return self.__age
	
	
